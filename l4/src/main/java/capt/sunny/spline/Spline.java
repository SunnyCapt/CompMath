package capt.sunny.spline;

public class Spline {
    private double a;
    private double b;
    private double c;
    private double d;
    private double x;

    public Spline() {
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }

    public double getD() {
        return d;
    }

    public double getX() {
        return x;
    }

    public void setA(double a) {
        this.a = a;
    }

    public void setB(double b) {
        this.b = b;
    }

    public void setC(double c) {
        this.c = c;
    }

    public void setD(double d) {
        this.d = d;
    }

    public void setX(double x) {
        this.x = x;
    }
}